export function getEnrichment(slug) {
  return ENRICHMENTS[slug] || null;
}

export function getLevelContent(slug, gameId) {
  const en = getEnrichment(slug);
  if (!en || !en.levelsByGameId) return null;
  return en.levelsByGameId[gameId] || null;
}
