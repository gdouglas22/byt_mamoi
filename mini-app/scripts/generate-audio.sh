#!/usr/bin/env bash
# Генератор аудио-реплик «Призовой звонок» через Microsoft Edge TTS.
# Запуск: bash scripts/generate-audio.sh
# Сохраняет mp3 в mini-app/assets/audio/phishing-3/

set -e
OUT_DIR="$(cd "$(dirname "$0")/.." && pwd)/assets/audio/phishing-3"
mkdir -p "$OUT_DIR"

# Голос «банковской автоответчицы» — Светлана, чуть медленнее обычной речи, +0% pitch
VOICE="ru-RU-SvetlanaNeural"
RATE="-5%"
PITCH="+0Hz"

declare -A SCRIPT=(
  ["01-intro"]="Здравствуйте! Это служба безопасности Сбербанка. Меня зовут Анна Петровна. Поздравляю — вы выиграли в розыгрыше нашего банка десять тысяч рублей."
  ["02-name"]="Чтобы начислить деньги, мне нужно подтвердить, что вы — это вы. Подскажите, пожалуйста, как вас зовут и сколько вам лет?"
  ["03-code"]="Замечательно. Сейчас на ваш телефон придёт эс-эм-эс с кодом из шести цифр. У нас очень мало времени — назовите код в течение тридцати секунд, и приз будет ваш."
  ["04-secret"]="И ещё одно — это очень важно. Никому не говорите про этот звонок. Ни маме, ни папе, ни друзьям. Это закрытая процедура, иначе приз сгорит."
  ["05-card"]="Отлично. И последнее: продиктуйте, пожалуйста, номер банковской карты, которая лежит у мамы или папы. Четыре группы по четыре цифры. Это нужно для перевода приза."
)

# Сортированный обход
for id in "01-intro" "02-name" "03-code" "04-secret" "05-card"; do
  text="${SCRIPT[$id]}"
  out="$OUT_DIR/$id.mp3"
  if [ -s "$out" ] && [ "$(stat -c%s "$out")" -gt 1000 ]; then
    echo "= $id: уже есть, пропускаю"
    continue
  fi
  echo "→ $id"
  edge-tts --voice="$VOICE" --rate="$RATE" --pitch="$PITCH" \
    --text "$text" --write-media "$out" 2>/dev/null
  if [ -s "$out" ]; then
    echo "  ✓ $(stat -c%s "$out") байт"
  else
    echo "  ✗ не сгенерилось"
  fi
done

echo ""
echo "✓ Аудио в $OUT_DIR"
ls -la "$OUT_DIR"
